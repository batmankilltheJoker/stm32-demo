void delay_us(uint16_t us)
{
	/*中断会打断微秒延时函数
	 *但计数器计数不会停止
	 *防止计数器增加到65535之后重新开始计数
	 */
	uint16_t differ =0xffff-us-5;//计算为最大计数值 65535 减去所需的延迟时间 us 和一个小的补偿值（5）,时钟
	
	HAL_TIM_Base_Start(&htim7);
	__HAL_TIM_SET_COUNTER(&htim7,differ);//设置定时器的计数器初始值
	
	while(differ<0xffff-5)
	{
		differ=__HAL_TIM_GET_COUNTER(&htim7);//更新 differ 值：在循环体中，调用 __HAL_TIM_GET_COUNTER 获取当前定时器 htim7 的计数值
	}
	
	HAL_TIM_Base_Stop(&htim7);
}
